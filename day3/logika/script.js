// operator logika merupakan operator dimana membandingkan 1 atau 2 lebih tipe data boolean

// and(akan bernilai true jika kedua sisinya true)
let a = 8

console.log((a%2) == 0 && a<10)

// or(akan bernilai true jika minimal salah satu sisinya true)
console.log((a%2) == 0 || a<10)

// not(akan membalikan boolean nya)
console.log(!((a%2) == 0))