// operator assigmnet atau kadang di sebut operator penugasan merupakan operator yang di tugaskan untuk bisa memasukkan nilai dari yang kanan ke dalam variabel di sebelah kirinya
// ada beberapa operator assigment

// sama dengan =
let a = 60
console.log("isi dari varibel nya adalah "+a)

// tambah sama dengan +=
 a += 60
console.log("Hasil tambah sama dengan nya adalah "+a)

// kurang sama dengan -=
a-= 60
console.log("Hasil kurang sama dengan nya adalah "+a)

// bagi sama dengan /=
a/= 2
console.log("Hasil bagi sama dengan nya adalah "+a)

// kali sama dengan *=
a*= 2
console.log("Hasil kali sama dengan nya adalah "+a)

// modulus sama dengan %=
a%=2
console.log("Hasil modulus sama dengan nya adalah "+a)