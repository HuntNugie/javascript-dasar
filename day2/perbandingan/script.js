// perbandingan
// operator perbandingan adalah operator yang membandingkan antara 2 nilai yang akhir nya akan menghasilkan nilai boolean
let a = 5
let b = 6

// sama dengan(perbandingan)
console.log(a==b)

// tidak sama dengan
console.log(a!=b)

// sama dengan strict
console.log(a===b)

// tidak sama dengan strict
console.log(a!==b)

// lebih besar dari
console.log(a>b)

// lebih kecil dari
console.log(a<b)

// lebih besar dari atau sama dengan
console.log(a>=b)

// lebih kecil dari atau sama dengna
console.log(a<=b)

