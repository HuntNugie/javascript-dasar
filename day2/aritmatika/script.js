// belajar aritmatika
// operator aritmatika adalah operator matematik dasar seperti pertambahan, pengurangan, perkalian dan lain lain

// pertambahan
let a = 50
let b = 5

let c = a+b

console.log("Hasil pertambahan dari "+a+" dan "+b+" adalah "+c)

// pengurangan
let d = a-b
console.log("Hasil pengurangan dari "+a+" dan "+b+" adalah "+d)

// perkalian
let e = a*b
console.log("Hasil perkalian dari "+a+" dan "+b+" adalah "+e)

// pembagian
let f = a/b
console.log("Hasil pembagian dari "+a+" dan "+b+" adalah "+f)

// modulus
let g = a%b
console.log("Hasil modulus(sisa bagi) dari "+a+" dan "+b+" adalah "+g)

// dalam operasi aritmatika ini yang di utamakan adalah kukabataku(kurung,kali,bagi,tambah,kurang)